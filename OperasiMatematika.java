public class OperasiMatematika {
    public static void main(String[] args) {
        
        int a = 100;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 100;
        c += 10; //augmented assigments
        System.out.println(c);
        c -= 10; //augmented assigments
        System.out.println(c);
        c *= 10; //augmented assigments
        System.out.println(c);

        //unary operator
        int d = 100;

        d++; //atau ++d 
        System.out.println(d);

        d--;
        System.out.println(d);


    }
}
